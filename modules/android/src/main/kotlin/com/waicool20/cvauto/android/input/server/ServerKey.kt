    companion object {
        private val puncMapping = mapOf(
            ' ' to KEY_SPACE,
            '~' to KEY_GRAVE,
            '!' to KEY_1,
            '@' to KEY_2,
            '#' to KEY_3,
            '$' to KEY_4,
            '%' to KEY_5,
            '^' to KEY_6,
            '&' to KEY_7,
            '*' to KEY_8,
            '(' to KEY_9,
            ')' to KEY_0,
            '_' to KEY_MINUS,
            '-' to KEY_MINUS,
            '+' to KEY_EQUALS,
            '=' to KEY_EQUALS,
            '{' to KEY_LEFT_BRACKET,
            '[' to KEY_LEFT_BRACKET,
            '}' to KEY_RIGHT_BRACKET,
            ']' to KEY_RIGHT_BRACKET,
            '|' to KEY_BACKSLASH,
            '\\' to KEY_BACKSLASH,
            ':' to KEY_SEMICOLON,
            ';' to KEY_SEMICOLON,
            '\'' to KEY_APOSTROPHE,
            '"' to KEY_APOSTROPHE,
            '<' to KEY_COMMA,
            ',' to KEY_COMMA,
            '.' to KEY_PERIOD,
            '>' to KEY_PERIOD,
            '?' to KEY_SLASH,
            '/' to KEY_SLASH
        )

        /**
         * Returns the corresponding key code needed to type this character
         *
         * @param name Name of character, eg. A, B, COMMA, ALT
         * @return [ServerKey]
         */
        fun findByName(name: String) = try {
            valueOf("KEY_${name.toUpperCase()}")
        } catch (e: IllegalArgumentException) {
            puncMapping.getOrDefault(name[0], KEY_UNKNOWN)
        }

        /**
         * Finds a Key with the given code.
         *
         * @param code The code to look for
         * @return [ServerKey]
         */
        fun findByCode(code: Long) = values().find { it.code == code }
    }
